"""Агент «Хвороби рослин».

Зараз — окремий ReAct-агент для перевірки RAG end-to-end.
Згодом стане вузлом swarm-графа (додамо інструменти-handoff до інших спеціалістів).
"""

from langchain_openai import ChatOpenAI
from langgraph.checkpoint.memory import MemorySaver
from langgraph.prebuilt import create_react_agent

import settings
from tools.rag import search_diseases

SYSTEM_PROMPT = """Ти — фахівець з хвороб рослин аграрного довідкового сайту.
Допомагаєш відвідувачу визначити хворобу за симптомами та підказуєш агротехнічні
заходи захисту.

ПРАВИЛА:
- Перед відповіддю ЗАВЖДИ викликай інструмент search_diseases. Не вигадуй назв
  хвороб, ознак, причин чи заходів — спирайся лише на знайдене.
- За симптомом називай 1–3 найімовірніші хвороби й познач, що це попередня версія.
  Якщо бракує даних — стисло уточни культуру або деталь симптому (одне питання за раз).
- Для КОЖНОЇ згаданої хвороби давай посилання (URL зі знайдених джерел). Відповідь
  без посилання неповна.
- Заходи захисту наводь із розділу «Заходи захисту» знайденого джерела.
- Підбір конкретних препаратів і доз — НЕ твоя зона. Якщо користувач хоче препарати,
  поясни, що це окремий крок (підбір препаратів), і що далі допоможе спеціаліст із препаратів.
- Відповідай стисло, природною українською. Якщо релевантного нічого не знайдено —
  чесно скажи про це й попроси уточнити симптом.
"""


def build_disease_agent():
    model = ChatOpenAI(
        model=settings.CHAT_MODEL,
        temperature=0.2,
        api_key=settings.OPENAI_API_KEY,
    )
    return create_react_agent(
        model,
        tools=[search_diseases],
        prompt=SYSTEM_PROMPT,
        checkpointer=MemorySaver(),
    )
